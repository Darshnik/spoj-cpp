#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

int main()
{
    int t;
    scanf("%d", &t);
    while (t--)
    {
        int n;
        scanf("%d", &n);
        vector<int> nums(n,0);
        for (int i=0; i<n; i++)
        {
            scanf("%d", &nums[n]);
        }
    }
    return 0;
}